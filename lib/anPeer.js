// –ê–Ω–∞–ª–∏–∑–∞—Ç–æ—Ä —Å–æ–æ–±—â–µ–Ω–∏–π –æ—Å–Ω–æ–≤–Ω–æ–π –∏–≥—Ä—ã
module.exports = function(msg) {
  let st  = global.st
  let mat = msg.m.match

  // –ú–µ—Å—Ç–Ω–æ—Å—Ç–∏
  if ( mat('–ß–∞—Ç –ª–∞–≥–µ—Ä—è')
    || mat('–¢—ã –≤–µ—Ä–Ω—É–ª—Å—è –≤ —Å–≤–æ–π –ª–∞–≥–µ—Ä—å.')
  ) st.cite      = '–ª–∞–≥–µ—Ä—å'

  if ( mat('—É—é—Ç–Ω—ã–π –≥–æ—Ä–æ–¥ –†–∏–Ω–æ,'))
    st.cite      = '—Ä–∏–Ω–æ'

  if ( mat('–¢–≤–æ–π –ø—É—Ç—å –ø—Ä–µ–≥—Ä–∞–¥–∏–ª –∏—Å–ø–æ–ª–∏–Ω—Å–∫–∏—Ö —Ä–∞–∑–º–µ—Ä–æ–≤ –º–æ–Ω—Å—Ç—Ä.')
    || mat('–≤ —ç—Ç–æ—Ç —Ä–∞–∑ —Ç—ã –Ω–µ –ø–æ–ª—É—á–∏–ª —Å–¥–∞—á–∏.')
  ) st.cite      = '–≥–∏–≥–∞–Ω—Ç'

  if ( mat('üìØüö∑ ‚ù§Ô∏è')
    || mat('üìØ‚ù§Ô∏è')
    || mat('üìØüö∑ –ë—ç—Ç-–ø–µ—â–µ—Ä–∞')
  ) st.cite      = '–¥–∞–Ω–∂'

  if ( mat('–ì–∏–≥–∞–Ω—Ç –ø–æ–≤–µ—Ä–∂–µ–Ω, –ø—É—Ç—å –¥–∞–ª—å—à–µ –æ—Ç–∫—Ä—ã—Ç!')
    || mat('–¢—ã –æ–¥–µ—Ä–∂–∞–ª –ø–æ–±–µ–¥—É!')
    || mat('–¢—ã –≥–æ—Ç–æ–≤ —Å–Ω–æ–≤–∞ –æ—Ç–ø—Ä–∞–≤–∏—Ç—å—Å—è –≤ –ü—É—Å—Ç–æ—à—å!')
    || mat('üë£')
  ) {
    st.cite      = '–ø—É—Å—Ç–æ—à—å'
    st.view      = false
  }

  if (
       msg.m.match('–¢—ã –Ω–µ —Å–º–æ–∂–µ—à—å —É–≤–∏–ª—å–Ω—É—Ç—å –æ—Ç –ø—Ä–æ—Ç–∏–≤–Ω–∏–∫–∞')
    || msg.m.match('–¢–µ–±–µ –Ω–µ —É–π—Ç–∏ –æ—Ç –ø—Ä–æ—Ç–∏–≤–Ω–∏–∫–∞')
    || msg.m.match('–í–æ –≤—Ä–µ–º—è –≤—ã–ª–∞–∑–∫–∏ –Ω–∞ —Ç–µ–±—è –Ω–∞–ø–∞–ª')
  ) {
    st.cite      = '–ø—É—Å—Ç–æ—à—å'
    st.view      = false
    st.angyMob   = true
  }

  // –ú–æ–¥–∏—Ñ–∏–∫–∞—Ç–æ—Ä—ã –º–µ—Å—Ç–Ω–æ—Å—Ç–µ–π
  if (
       msg.m.match('–¢—ã –æ—Ü–µ–Ω–∏–ª –æ–±—Å—Ç–∞–Ω–æ–≤–∫—É –≤–æ–∫—Ä—É–≥.')
    || msg.m.match('–¢—ã –æ–≥–ª—è–¥–µ–ª—Å—è –≤–æ–∫—Ä—É–≥ —Å–µ–±—è.')
  ) st.view      = true

  if ( msg.m.match('–≤ —ç—Ç–æ—Ç —Ä–∞–∑ —É–∂–µ –±—É–∫–≤–∞–ª—å–Ω–æ.'))
    st.lowHealth = true

  if (
       msg.m.match('–¢—ã –æ–¥–µ—Ä–∂–∞–ª –ø–æ–±–µ–¥—É!')
    || msg.m.match('–¢—ã –≥–æ—Ç–æ–≤ —Å–Ω–æ–≤–∞ –æ—Ç–ø—Ä–∞–≤–∏—Ç—å—Å—è –≤ –ü—É—Å—Ç–æ—à—å!')
  ) st.angyMob   = false

  if ( msg.m.match('–¢—ã –≤—Å—Ç—Ä–µ—Ç–∏–ª –±—Ä–æ–¥—è—á–µ–≥–æ —Ç–æ—Ä–≥–æ–≤—Ü–∞,'))
    st.goodMob   = true

  st.dark        = Boolean(msg.m.match('üö∑'))

  // –õ–æ–≤–∏–º –∫–∏–ª–æ–º–µ—Ç—Ä–∞–∂
  let parse
  if (parse = msg.m.match(/üö∑.*üë£(\d+)–∫–º/s))
    st.x         = parse[1]
  if (parse = msg.m.match(/üö∑.*(\d+)\s?–∫–º/))
    st.x         = parse[1]
  if (parse = msg.m.match(/üë£(\d+)\s?–∫–º/))
    st.x         = parse[1]

  // –õ–æ–≤–∏–º –æ—á–∏—Å—Ç–∫—É
  if (msg.m.match('–ü–æ–ª—É—á–µ–Ω–æ'))
    st.garbage   = true
}
