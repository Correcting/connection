function foundShit(msg) {
  let parse = false
  for (let i=0; i<badGoods.length; i++) {
    parse = msg.m.match(new RegExp('–ü–æ–ª—É—á–µ–Ω–æ.*('+badGoods[i]+').*', 's'))
    if (parse) break
  }

  if (parse) {
    console.log(parse)
    if (parse[0].match('–æ–±–ª–æ–º–∫–∏')) return false
    else return parse[1]
  } else return false
}

function grabShit(msg) {
  let parse = false
  for (let i=0; i<badGoods.length; i++) {
    parse = msg.m.match(new RegExp(badGoods[i]+'.*(\\/dl_\\d+)'))
    if (parse) break
  }

  if (parse) return parse[1]
  else       return false
}
