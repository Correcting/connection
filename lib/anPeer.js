// –ê–Ω–∞–ª–∏–∑–∞—Ç–æ—Ä —Å–æ–æ–±—â–µ–Ω–∏–π –æ—Å–Ω–æ–≤–Ω–æ–π –∏–≥—Ä—ã
module.exports = function(msg) {
  let st  = global.st
  st.act  = false // –ü–æ—É–º–æ–ª—á–∞–Ω–∏—é –Ω–∏—á–µ–≥–æ –Ω–µ –¥–µ–ª–∞–µ–º –Ω–∞ –≤—Ö–æ–¥—è—â–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ

  // –ú–µ—Å—Ç–Ω–æ—Å—Ç–∏
  if ( msg.m.match('–ß–∞—Ç –ª–∞–≥–µ—Ä—è')
    || msg.m.match('–¢—ã –≤–µ—Ä–Ω—É–ª—Å—è –≤ —Å–≤–æ–π –ª–∞–≥–µ—Ä—å.')
  ) {
    st.act       = true
    st.cite      = '–ª–∞–≥–µ—Ä—å'
  }

  if ( msg.m.match('—É—é—Ç–Ω—ã–π –≥–æ—Ä–æ–¥ –†–∏–Ω–æ,')) {
    st.act       = true
    st.cite      = '—Ä–∏–Ω–æ'
  }

  if ( msg.m.match('–¢–≤–æ–π –ø—É—Ç—å –ø—Ä–µ–≥—Ä–∞–¥–∏–ª –∏—Å–ø–æ–ª–∏–Ω—Å–∫–∏—Ö —Ä–∞–∑–º–µ—Ä–æ–≤ –º–æ–Ω—Å—Ç—Ä.')
    || msg.m.match('–≤ —ç—Ç–æ—Ç —Ä–∞–∑ —Ç—ã –Ω–µ –ø–æ–ª—É—á–∏–ª —Å–¥–∞—á–∏.')
  ) {
    st.act       = true
    st.cite      = '–≥–∏–≥–∞–Ω—Ç'
  }

  if ( msg.m.match('üìØüö∑ ‚ù§Ô∏è')
    || msg.m.match('üìØ‚ù§Ô∏è')
    || msg.m.match('üìØüö∑ –ë—ç—Ç-–ø–µ—â–µ—Ä–∞')
  ) {
    st.act       = true
    st.cite      = '–¥–∞–Ω–∂'
  }

  if ( msg.m.match('–ì–∏–≥–∞–Ω—Ç –ø–æ–≤–µ—Ä–∂–µ–Ω, –ø—É—Ç—å –¥–∞–ª—å—à–µ –æ—Ç–∫—Ä—ã—Ç!')
    || msg.m.match('–¢—ã –≥–æ—Ç–æ–≤ —Å–Ω–æ–≤–∞ –æ—Ç–ø—Ä–∞–≤–∏—Ç—å—Å—è –≤ –ü—É—Å—Ç–æ—à—å!')
    || msg.m.match('üëÅ–û—Å–º–æ—Ç—Ä–µ—Ç—å—Å—è')
  ) {
    st.act       = true
    st.cite      = '–ø—É—Å—Ç–æ—à—å'
  }

  if ( msg.m.match('–í–æ –≤—Ä–µ–º—è –≤—ã–ª–∞–∑–∫–∏ –Ω–∞ —Ç–µ–±—è –Ω–∞–ø–∞–ª')
    || msg.m.match('–¢—ã –Ω–µ —Å–º–æ–∂–µ—à—å —É–≤–∏–ª—å–Ω—É—Ç—å –æ—Ç –ø—Ä–æ—Ç–∏–≤–Ω–∏–∫–∞')
    || msg.m.match('–¢—ã –∫—Ä–∞–µ–º –≥–ª–∞–∑–∞ –∑–∞–º–µ—Ç–∏–ª')
  ) {
    st.act       = true
    st.cite      = '–ø—É—Å—Ç–æ—à—å'
    st.angryMob  = true
  }

  // –ú–æ–¥–∏—Ñ–∏–∫–∞—Ç–æ—Ä—ã –º–µ—Å—Ç–Ω–æ—Å—Ç–µ–π
  if ( msg.m.match('–≤ —ç—Ç–æ—Ç —Ä–∞–∑ —É–∂–µ –±—É–∫–≤–∞–ª—å–Ω–æ.')) {
    st.act       = true
    st.lowHealth = true
  }

  if ( msg.m.match('–¢—ã –æ–¥–µ—Ä–∂–∞–ª –ø–æ–±–µ–¥—É!')) {
    st.act       = true
    st.angryMob  = false
  }

  if ( msg.m.match('–¢—ã –≤—Å—Ç—Ä–µ—Ç–∏–ª –±—Ä–æ–¥—è—á–µ–≥–æ —Ç–æ—Ä–≥–æ–≤—Ü–∞,')) {
    st.act       = true
    st.goodMob   = true
  }

  st.dark        = Boolean(msg.m.match('üö∑'))

  // –õ–æ–≤–∏–º –∫–∏–ª–æ–º–µ—Ç—Ä–∞–∂ (–Ω–µ –≤—ã–∑—ã–≤–∞–µ—Ç –Ω–µ–ø–æ—Å—Ä–µ–¥—Å—Ç–≤–µ–Ω–Ω–æ–≥–æ –¥–µ–π—Å—Ç–≤–∏—è
  // (st.act –æ—Å—Ç–∞—ë—Ç—Å—è false), —Ç—Ä–µ–±—É–µ—Ç—Å—è —Å—é–∂–µ—Ç–Ω–∞—è —Ä–µ–∞–∫—Ü–∏—è)
  let parse
  if (parse = msg.m.match(/üö∑.*üë£(\d+)–∫–º/s))
    st.x         = parse[1]
  if (parse = msg.m.match(/üö∑.*(\d+)\s?–∫–º/))
    st.x         = parse[1]
  if (parse = msg.m.match(/üë£(\d+)\s?–∫–º/))
    st.x         = parse[1]

  // –õ–æ–≤–∏–º –æ—á–∏—Å—Ç–∫—É
  if ( msg.m.match('–ü–æ–ª—É—á–µ–Ω–æ'))
    st.garbage   = '–ø–æ–ª—É—á–µ–Ω–æ'
  if ( msg.m.match('/dl_')) {
    st.act       = true // –ò–ª–∏ –º—É—Å–æ—Ä, –∏–ª–∏ –∏–¥—Ç–∏ –¥–∞–ª—å—à–µ
    st.garbage   = grabShit(msg)
  }
  if (parse = msg.m.match(/\/del_\d+/)) {
    st.act       = true
    st.garbage   = parse[0]
  }
}

function grabShit(msg) {
  const badGoods = [
    'BFGzzv-4000',     '–ë–æ–µ–≤–∞—è –±—Ä–æ–Ω—è',
    '–ê—Ä–º–∞–≥–µ–¥–¥–µ—Ü',      '–ü–æ—Ç—Ä–æ—à–∏—Ç–µ–ª—å',
    '–ë–æ–µ–≤–∞—è –±—Ä–æ–Ω—è',    '–ë—Ä–æ–Ω—è –±—Ä–∞—Ç—Å—Ç–≤–∞',
    '–ö–∏–Ω–∂–∞–ª',          '–ö–æ–∂–∞–Ω–Ω—ã–π –Ω–∞–≥—Ä—É–¥–Ω–∏–∫',
    '–ö–æ–∂–∞–Ω—ã–π –∂–∏–ª–µ—Ç',   '–¢–∏—Ç–∞–Ω–æ–≤—ã–µ —â–∏—Ç–∫–∏',
    '–ì—Ä–∞–≤–∏–ø—É—à–∫–∞',      '–ú–∞—á–µ—Ç–µ',
    '–®–∏–ø–∞—Å—Ç–∞—è –±–∏—Ç–∞',   '–õ–∞–∑–µ—Ä–Ω—ã–π —Ç–µ—Å–∞–∫',
    '–®–ª—è–ø–∞ –º–∏–Ω–∏—Ç–º–µ–Ω–∞', '–ú–æ—Ç–æ-–∑–∞—â–∏—Ç–∞',
    '–ü–ª–æ—Ç–Ω—ã–π –∫–∞–ø—é—à–æ–Ω', '–ü—Ä–æ—Ç–∏–≤–æ–≥–∞–∑',
    '–°—É–ø–µ—Ä–º–æ–ª–æ—Ç',      '–§–∞–ª–º–µ—Ä—Å–∫–∏–π –∫–ª–∏–Ω–æ–∫',
    '–§—É—Å—Ä–æ–Ω–µ—Ç',        '–ö–æ–ø—å–µ',
    '–£—à–∞–Ω–∫–∞',          '–í—è–∑–∞–Ω–∞—è —à–∞–ø–∫–∞',
    '–•–ª—ã—Å—Ç',           'üõ†–ú—É–ª—å—Ç–∏–∑–∞—â–∏—Ç–∞',
    '–ö–∏—Å—Ç–µ–Ω—å',         '–≠–ª–µ–∫—Ç—Ä–æ–º–µ—á'
  ]

  let parse
  for (let i=0; i<badGoods.length; i++) {
    parse = msg.m.match(new RegExp(badGoods[i]+'.*(\\/dl_\\d+)'))
    if (parse) break
  }
  if (parse) return parse[1]
  else       return false
}
